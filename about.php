      SUBMISSION INSTRUCTIONS/GUIDELINES:
      <br><br>
      <strong>Age Limit:</strong>
      <br>
      For the purpose of this zine, we are defining ‘youth’ as 24 years and younger.
      While you folks older than that may be young at heart, and undoubtably have wonderful things to share, we unfortunately will not be able to publish your work.
      It is very important to us that this is a fully youth-powered endeavor, as a way of proving that young people are capable of taking things into their own hands.
      <br><br>

      <strong>Individual Submissions:</strong>
      <br>
      Currently, we publish submissions in the following categories:
      <br>
      Visual Art (painting, drawing, photography, etc.),
      <br>
      Creative Writing (poem, vignettes, short stories, etc.),
      <br>
      Non-Fiction (short research papers, political commentaries,
      <br>
      and opinion pieces on current events, etc.).
      <br><br>
      If you are interesting in submitting your work, check to see if we're accepting submissions. If we are, simply email your work to us at weareneonmag@gmail.com by clicking the 'Submit' link above.
      <br><br>


      Visual Art Submissions:
      <br>
      Please submit Visual Art as scans or high-quality photos in .jpeg form at 600 dpi, as this makes formatting the zine much more manageable. All submissions should be formatted in a professional, portfolio-grade manner. Please include the dimensions of your artwork, as well as a title and any other additional information you'd like to be published with it.
      <br><br>

      Writing Submissions:
      <br>
      Writing can be submitted as text in the body of your email, although we'd prefer it as a .pdf. As with visual art, please include the title, and any information about the piece that you'd like to have included. Please specify which writing category your piece falls under (Creative or Non-Fiction).
      <br><br>

      ALL Visual Art and Writing MUST be original work. Please EDIT your writing carefully, and make sure that you are submitting HIGH-QUALITY images of your artwork.
      Faults in our formatting/publishing of your work are most easily avoided if you are clear and careful when you submit your work.
      <br><br>

      Please include the name you would like your work to published under (specify if you would like to publish anonymously), and any other personal information you would like to include alongside your name (including your age and location [city, state] is suggested, so that we can demonstrate to our readers the breadth and diversity of young talent).
      We will contact you if your work is to be published, and will mail you a free copy of the issue your work is featured in.
      <br><br>

      Featured Youth:
      Each issue will also contain a piece or two, written by the founders of We Are Neon, highlighting a youth-powered endeavor that we find noteworthy. This could be a single young entrepreneur doing something interesting, a youth-founded non-for-profit organization, young people participating in social activism, a group of young people working on some innovative and exciting new project, etc.. If you feel that you, or someone you know is involved in something that might be worth writing a short article about, please contact us! We would love to interview you for our publication.
