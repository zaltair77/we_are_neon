    <p>
      Young people often feel disenfranchised and condescended to.
      Often, they are not seen by adults as autonomous individuals with valid, innovative, and powerful thoughts of their own.
      There are not many opportunities for young people to showcase their work, whether it be literature, visual art, music, or some exciting initiative they are working on.
      <br><br>
      However, as we know, young people are wildly talented, innovative, intelligent, capable of being agents of change, and are integral to the health of a community.
      We Are Neon is a way for young people to share their ideas and self-expression with the world, and through doing so, become more empowered and recognized as active, valuable global members.
      It is a way to be inspired, and inspire others.
      <br>
